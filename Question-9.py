# Name the keywords, used as data in the python script.

'''
1. `False`: Represents the boolean value "false."
2. `None`: Represents the absence of a value or a null value.
3. `True`: Represents the boolean value "true."
4. `and`: A logical operator used for boolean "and" operations.
5. `as`: Used in the context of the `import` statement to create an alias for a module.
6. `assert`: Used for debugging purposes to check if a condition is true. Raises an exception if the condition is false.
7. `break`: Used to exit from a loop prematurely.
8. `class`: Defines a new class.
9. `continue`: Used to skip the current iteration of a loop and continue with the next one.
10. `def`: Defines a new function.
11. `del`: Used to delete references to objects.
12. `elif`: Short for "else if," used in conditional statements.
13. `else`: Used in conditional statements as a fallback option.
14. `except`: Used in try-except blocks to handle exceptions.
15. `finally`: Used in try-except blocks to define code that will be executed regardless of whether an exception occurs or not.
16. `for`: Used to create loops.
17. `from`: Used in the context of the `import` statement to specify which module members to import.
18. `global`: Used to declare global variables within a function.
19. `if`: Used to define conditional statements.
20. `import`: Used to import modules into the current script.
21. `in`: Used to check if a value is present in a container (e.g., a list, tuple, or dictionary).
22. `is`: Used to compare object identities (if two objects refer to the same memory location).
23. `lambda`: Used to create anonymous functions.
24. `nonlocal`: Used to declare nonlocal variables within a nested function.
25. `not`: A logical operator used for boolean "not" operations.
26. `or`: A logical operator used for boolean "or" operations.
27. `pass`: Used as a placeholder for empty code blocks.
28. `raise`: Used to raise exceptions.
29. `return`: Used to return values from functions.
30. `try`: Used to start a try-except block to handle exceptions.
31. `while`: Used to create a loop that continues as long as a certain condition is true.
32. `with`: Used in the context of the `with` statement to manage resources.
33. `yield`: Used in generator functions to produce a sequence of values.
'''